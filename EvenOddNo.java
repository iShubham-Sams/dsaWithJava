
import java.util.Scanner;

public class EvenOddNo {
    public static void main(String[] args) {
        int a;
        Scanner m =new Scanner(System.in);
        a=m.nextInt();
        if(a%2==0){
            System.out.println("a is even no");
        }else{
            System.out.println("a is not even");
        }
    }
    
}